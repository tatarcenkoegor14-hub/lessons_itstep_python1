points = int(input("Введіть кількість отриманих балів (0-100): "))

# Визначення оцінки
if 0 <= points <= 49:
    print("Оцінка: незадовільно")
elif 50 <= points <= 69:
    print("Оцінка: задовільно")
elif 70 <= points <= 89:
    print("Оцінка: добре")
elif 90 <= points <= 100:
    print("Оцінка: відмінно")
else:
    print("Помилка: введіть число від 0 до 100")